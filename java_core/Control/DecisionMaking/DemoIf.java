public class DemoIf {
    public static void main(String[] args) {
        int num1 = 0;
        int num2 = 3;
        
        if (num1 < num2) {
            System.out.println("Num1 = " + num1 + " is less than Num2 = " + num2);
        } else {
            System.out.println("Num1 = " + num1 + " is greater than or equal to Num2 = " + num2);
        }
    }
}